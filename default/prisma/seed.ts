// Helper function to map US shoe sizes to enum sizes
function mapSizeToEnum(size: string): Size {
  const sizeMap: Record<string, Size> = {
    // US shoe sizes
    'US 5': Size.XS,
    'US 6': Size.XS,
    'US 7': Size.S,
    'US 8': Size.S,
    'US 9': Size.M,
    'US 10': Size.L,
    'US 11': Size.XL,
    'US 12': Size.XXL,
    'US 13': Size.XXXL,
    'US 14': Size.XXXXL,
    // Apparel sizes
    'XS': Size.XS,
    'S': Size.S,
    'M': Size.M,
    'L': Size.L,
    'XL': Size.XL,
    'XXL': Size.XXL,
    'XXXL': Size.XXXL,
    'XXXXL': Size.XXXXL,
  };
  return sizeMap[size] || Size.ONE_SIZE;
}

// Helper function to generate random image URLs based on category
function generateImageUrl(categorySlug: string): string {
  const seed = `${categorySlug}-${Math.floor(Math.random() * 1000)}`;
  return `https://picsum.photos/seed/${seed}/500/500`;
}

